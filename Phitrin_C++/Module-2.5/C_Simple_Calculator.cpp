#include<bits/stdc++.h>
using namespace std;

int main()
{
    long long int a,b;
    cin>>a>>b;

    long long int sum=a+b;
    cout<<a<<" + "<<b<<" = "<<sum<<endl;
    long long int mul=a*b;
    cout<<a<<" * "<<b<<" = "<<mul<<endl;
    long long int dif=a-b;
    cout<<a<<" - "<<b<<" = "<<dif<<endl;
    return 0;
}